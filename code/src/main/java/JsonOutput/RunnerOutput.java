package JsonOutput;

public class RunnerOutput {
	private String name;
	private float fees;
	
	public RunnerOutput() {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public float getFees() {
		return fees;
	}

	public void setFees(float fees) {
		this.fees = fees;
	}
	
}
